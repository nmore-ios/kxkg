import Foundation

//NSLog使用指示符

//指示符	描述
//%@	对象，String、Array、Dictionary等都是对象
//%%	『%』字符
//%d,%D	带符号的32位整数
//%u,%U	无符号的32位整数
//%x,%X	无符号的32位整数，按照16进制输出
//%o,%O	无符号的32位整数，按照8进制输出
//%f	64位浮点数
//%e,%E	64位浮点数，按照科学记数法输出
//%c	八位无符号字符
//%C	16位Unicode字符
//%a,%A	64位浮点数，按照科学记数法输出
//%F	64位浮点数，按照16进制输出